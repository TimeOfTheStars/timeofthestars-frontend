export function getPlayerPhoto(id: number): string {
    if (playerPhotos[id] === '') {
        return '/заявочные фото/defaultplayer.jpg'
    }
    return playerPhotos[id] ?? '/заявочные фото/defaultplayer.jpg'
}

export function getScan(id: number): string {
    if (scans[id] === '') {
        return ''
    }
    return scans[id] ?? ''
}
